    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           String nomeLogin = jTextField1.getText(); // ENTRADA DE DADOS
           String senha1 = jTextField2.getText();// ENTRADA DE DADOS
           
           if((nomeLogin.equals(""))&&(senha1.equals(""))){//SE O CAMPO DE TEXTO 1 ESTIVER VAZIO, E O CAMPO DE TEXTO 2 ESTIVER VAZIO
                jLabel5.setText("Por favor digite a senha e login");
          }else{
                if(nomeLogin.equals(usuario) && senha1.equals(senha)){
                    jLabel5.setText("Login efetuado com sucesso ");
                    if(jogada == null){ // se é igual a vazio é porque não foi criado nem um objeto
                        jogada = new Jogo();
                        jogada.setLocationRelativeTo(null); // Centralizando o objeto jogada ou melhor a tela do jogo
                        jogada.setResizable(false); // não pode redimensionar a tela
                        jogada.setVisible(true); // deixando o bjeto tela jogo visivel
                    }else{ // já existe um objeto criado
                         /*COMO JÁ EXISTE UM OBJETO CRIADO NÃO HÁ NESCESSIDADE DE CRIAR OUTROS OBJETOS PARA OCUPAR
                    ESPAÇO NA MEMÓRIA */
                        jogada.setLocationRelativeTo(null); // Centralizando o objeto jogada ou melhor a tela do jogo
                        jogada.setResizable(false); // não pode redimensionar a tela
                        jogada.setVisible(true); // deixando o bjeto tela jogo visivel
                    }
                   
                }else{
                    matriz.objetoExiste();
                    jLabel5.setText("ERRO! Login ou senhan");
                    
                }
           }
           jogada.indoParaTelaJogo(this);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if( this.cadastro == null){ // senão existir um objeto
            cadastro = new Cadastro(); // cria uma objeto cadastro
            cadastro.setLocationRelativeTo(null); // Centralizando a tela 
            cadastro.setResizable(false); // Desabilitando a opção de redimensionar a tela
            cadastro.setVisible(true); // deixando o bjeto visivel
            
        }else{
            cadastro.setLocationRelativeTo(null);
            cadastro.setResizable(false);
            cadastro.setVisible(true);
        }
        cadastro.indoParaCadastro(this); // CHAMADA AO MÉTODO DA CLASSE CADASTRO
    }//GEN-LAST:event_jButton2ActionPerformed

