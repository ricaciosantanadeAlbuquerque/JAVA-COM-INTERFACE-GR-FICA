    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String nome = jTextField1.getText();
        String email = jTextField2.getText();
        String telefone = jTextField3.getText();
        String nomeUsuario = jTextField4.getText();
        String senha = jTextField5.getText();
        String confirmaSenha = jTextField6.getText();
        
        
        if(nome == "" && email == "" && telefone == "" && nomeUsuario == "" && senha == ""
                && confirmaSenha == ""){
            JOptionPane.showMessageDialog(null, "Por favor preencha os campos ");
        }else{
            if( senha.equalsIgnoreCase(confirmaSenha)){
                dados = new DadosCadastro(nome, email, telefone, nomeUsuario, senha, confirmaSenha);
                vetor.cadastrar(dados);// CHAMADA AO MÉTODO CADASTRA DA CLASSE estruturaDados, joga os dados para o arrayList
                tela1.salvarSenha(nomeUsuario, confirmaSenha); // chamada ao método da classe [TelaLogin] passando nome e senha confirmada
                  jLabel7.setText(vetor.quantidadeCadastro());    
                  JOptionPane.showMessageDialog(this,"Cadastro realizado com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null,"ERRO! Senha inválida preencha o"
                        + " campo de confirmação novamente por favor");
                jTextField6.setText("");
            }
        }
        jTextField1.setText(""); //Limpado os campos de texto
                  jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                      jTextField6.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

