    public void lerDados(){
        ArrayList<Cliente> novoVetor = new ArrayList();
        Gerente novoGerente = new Gerente();
        novoVetor = novoGerente.listarGeral();
        
                  boolean busca = false;
                  
            for(Cliente p :novoVetor){
              if(p.getNome().equals(this.login) && p.getSenha().equals(this.senha)){
               jTextField1.setText(p.getNome());
               jTextField2.setText(p.getCpf());
               jTextField3.setText(p.getCelular());
               jTextField4.setText(p.getSexo());
               jTextField5.setText(p.getSenha());
               jLabel8.setText(String.valueOf(p.getId()));
               busca = true;
                break;
              }
           }
           if(busca == false){
              JOptionPane.showMessageDialog(null,"Erro!"); 
           }
           
    }
   
