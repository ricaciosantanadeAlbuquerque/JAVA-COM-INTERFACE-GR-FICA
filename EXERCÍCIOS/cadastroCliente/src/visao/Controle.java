    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Gerente novoGerente = new Gerente();
       
      this.setLogin(jTextField1.getText());
      this.setSenha(jTextField2.getText());
       Cliente novoCiente = new Cliente(this.Login,this.senha);
       ArrayList<Cliente> dados = novoGerente.litarClientes(novoCiente);
       boolean abrir = false;
        for(Cliente pessoa: dados){
                if(pessoa.getNome().equals(this.Login) && pessoa.getSenha().equals(this.senha)){
                    ler(pessoa.getNome());
                   abrir = true;
                   break;
                }
        }
        
        if(abrir == true){
             Dados clinte = new Dados(this.Login,this.senha);
             jDesktopPane1.add(clinte);
             clinte.setVisible(true); 
        }else{
            JOptionPane.showMessageDialog(null,"Senha ou login inválido! ");
        }
        jTextField1.setText("");
        jTextField2.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

