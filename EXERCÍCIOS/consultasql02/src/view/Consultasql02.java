    private void inicializar() {
          db = new Fabrica();
            if(jTextField1.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Campo vazio !! preencha o campo por favor ");
            }else{
                 if(db.getConnection()){
              try {
                  PreparedStatement statement = db.connection.prepareStatement(jTextField1.getText());
                  ResultSet resultset = statement.executeQuery();
                  DefaultTableModel tabelaModelo = new DefaultTableModel(new Object[]{},0);
                  int qtdColunas = resultset.getMetaData().getColumnCount();
                  for(int indice = 1; indice <= qtdColunas;indice++){
                      tabelaModelo.addColumn(resultset.getMetaData().getColumnName(indice));
                  }
                  jTable1 = new JTable(tabelaModelo);
                 DefaultTableModel tabelaFinal = (DefaultTableModel) jTable1.getModel();
                 
                 while(resultset.next()){
                     Object[] dados = new Object[qtdColunas]; //o tamanho da quantidadede  columns retornadas
                     for(int i = 1; i <= qtdColunas;i++){
                         dados[i - 1] = resultset.getObject(i);
                     }
                     tabelaFinal.addRow(dados);
                     System.out.println();
                     jScrollPane1.setViewportView(jTable1);
                 }
                 resultset.close();
                 statement.close();
                 db.getClose();
              } catch (SQLException ex) {
                  Logger.getLogger(Consultasql02.class.getName()).log(Level.SEVERE, null, ex);
              }
          }else{
              JOptionPane.showMessageDialog(null,"falha na conexão o sistema será fechado");
              System.exit(0);
          }
            }
    }
