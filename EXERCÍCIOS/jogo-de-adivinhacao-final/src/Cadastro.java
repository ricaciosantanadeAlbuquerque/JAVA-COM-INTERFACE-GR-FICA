    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String NomeUsuario = jTextField1.getText(); // captura dos dados
        String senha = jTextField2.getText();// captura dos dados
        String confirmaSenha = jTextField3.getText();// captura dos dados
        
        if(senha.equals(confirmaSenha)){
            if(jTextField1.getText().equals("") && jTextField2.getText().equals("") && jTextField3.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Por favor preencha os campos ! ");
            
         }else{
                if(this.jogador == null){// se não tem objeto eu crio um 
                    jogador = new Usuario(NomeUsuario,senha,confirmaSenha); // objeto jogador
                    generico.cadastrar(jogador);
                }else{ // agora se já tem um objeto eu crio um novo objeto se não ele vai sempre usar o primeiro que foi criado
                    jogador = new Usuario(NomeUsuario,senha,confirmaSenha);
                       generico.cadastrar(jogador);
                }
            
         }
        
        
        }else{
             JOptionPane.showMessageDialog(this,"ERRO! senha não confere");
        } 
         //========LIMPANDO OS CAMPOS===========================
         jTextField1.setText("");
         jTextField2.setText("");
         jTextField3.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

