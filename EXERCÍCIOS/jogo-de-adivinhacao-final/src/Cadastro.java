    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       for(int i =0; i < generico. estrutura.size();i++ ){ // enquanto i for menor do que o ArrayList() estrutura
           JOptionPane.showMessageDialog(null,i+" "+generico.estrutura.get(i)); // retorna o que tem na posição i
          try{
               String resultado = JOptionPane.showInputDialog("Se deseja deletar está posição ? digite [oks] ");
           if(resultado.equalsIgnoreCase("oks")){
               Usuario posicao = generico.estrutura.get(i);
               generico.deletar(posicao);
           }
          }catch(Exception erro){
              JOptionPane.showMessageDialog(null,"Operação cancelada! ");
              
          }
       }
       
    }//GEN-LAST:event_jButton4ActionPerformed

   
