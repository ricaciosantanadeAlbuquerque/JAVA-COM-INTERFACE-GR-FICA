    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if(jTable1.getSelectedRow() != -1){
           Produto produto = new Produto();
           Gerente gerente = new Gerente();
          produto.setDescricao(jTextField1.getText());
          produto.setQtd(Integer.parseInt(jTextField2.getText()));
          produto.setPreco(Double.parseDouble(jTextField3.getText()));
          produto.setId((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
          gerente.atualizar(produto);
          jTextField1.setText("");
          jTextField2.setText("");
          jTextField3.setText("");
          lerTabela();
          
       }else{
           JOptionPane.showInputDialog(null,"Selecione uma opção !");
       }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTable1.getSelectedRow() != -1){ // se é difrente de -1 é porque algo foi selecionado
            Produto produto = new Produto();
            Gerente gerente = new Gerente();
          produto.setId((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
          gerente.deletar(produto);
          jTextField1.setText("");
          jTextField2.setText("");
          jTextField3.setText("");
          lerTabela();
          
        }else{
            JOptionPane.showMessageDialog(null,"Erro! valor não selecionado !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    
    public void lerTabela(){
        DefaultTableModel  modelo = (DefaultTableModel)jTable1.getModel();
        modelo.setNumRows(0);
     Gerente gerente = new Gerente();
     
     for(Produto p: gerente.listar()){
         modelo.addRow(new Object[]{p.getId(),p.getDescricao(),p.getQtd(),p.getPreco()});
     }
     
    }
